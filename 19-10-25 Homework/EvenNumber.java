class EvenNumber
{
	public static void main(String[] args)
	{
		int num = 26;
		
		if(num%2==0)
		{
			System.out.println(num+" is a even number");
		}
		else if(num%2==1)
		{
			System.out.println(num+" is a odd number");
		}
		else
			System.out.println("You are entered a wrong number");
	}
}

