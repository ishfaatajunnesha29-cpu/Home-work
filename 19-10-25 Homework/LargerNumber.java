class LargerNumber
{
	public static void main(String[] args)
	{
		int a = 15;
		int b = 38;
		
		if(a>b)
		{
			System.out.println(a+" "+"is a Larger Number than"+" "+b);
		}
		else if(a<b)
		{
			System.out.println(b+" "+"is a Larger Number than"+" "+a);
		}
		else
		{
			System.out.println("You are Entered a wrong number");
		}
	}
}