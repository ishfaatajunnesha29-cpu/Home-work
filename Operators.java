class Operators
{
	public static void main(String[] args)
	{
		short a = 10;
		short b = 5;
		int c = a+b;
		System.out.println("The Addition of Two Values is:"+" "+c);
		
		short d = 30;
		short e = 18;
		int f = d-e;
		System.out.println("The Subtraction of Two Values is:"+" "+f);
		
		short g = 25;
		short h = 4;
		int i = g*h;
		System.out.println("The Product of Two Values is:"+" "+i);
		
		short j = 125;
		short k = 5;
		int l = j/k;
		System.out.println("The Division of Two Values is:"+" "+l);
		
		short m = 99;
		short n = 10;
		int o = m%n;
		System.out.println("The Modulas of Two Values is:"+" "+o);
				
	}
}